    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id1= request.getParameter("id");
        int id= Integer.parseInt(id1);
        ListUser l= new ListUser();
        int n=0;
        try {
            n= l.GetDiscountPercentByID(id);
        } catch (SQLException ex) {
            Logger.getLogger(GetDiscountPercentNgController.class.getName()).log(Level.SEVERE, null, ex);
        }
       ArrayList<OrderDetails> tmp = (ArrayList<OrderDetails>) request.getSession().getAttribute("cart");
       int n2=0;
       for(OrderDetails o : tmp)
       {
            
           n2+=(int)o.getTotal();
       }
        double k=(n*1.0/100);
        double m=n2*k;
        String json = new Gson().toJson((int)m);
        response.setContentType("application/json; charset=utf-8");
        response.getWriter().write(json);
    }

